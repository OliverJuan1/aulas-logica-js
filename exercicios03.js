// Crie uma função que recebe um parâmetro de nota e verfica em um condicional se a nota for maior que 7, o aluno está aprovado, senão, o aluno etá reprovado

function resultadoFinal(nota) {
    if (nota >= 7) {
        console.log("Resultado Final é: Aprovado")
    } else {
        console.log("Resultado Final é: Reprovado")
    }
}
resultadoFinal(6)
